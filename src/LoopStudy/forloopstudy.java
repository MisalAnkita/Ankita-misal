package LoopStudy;

public class forloopstudy {

	public static void main(String[] args)
	{
		// i want to print 3 table
		for(int a=3;a<=30;a=a+3)
		{
			System.out.println(a);
			
		}
		// i want to print Ankita for 7 time
		
		 for(int b=1;b<=7;b++)
		 {
			 System.out.println(b);
		 }
		
		// i want to print 2 table in reverse
		 for(int c=30;c>=3;c=c-2)
		 {
			 System.out.println(c);
		 }
		 // i want print 10 table in reverse
		 for(int d=100;d>=10;d=d-10)
		 {
			 System.out.println(d);

		 }
		
		
		
	}

}