package LoopStudy;

public class whileLoop 
{

public static void main(String[] args)
	
	{
		// i want to print 1-10 number
     int a=1;
     while(a<=10)
        {
    	  System.out.println(a);
    	  a++;
         }
     // i want to print 2 table
      int b=2;
      while(b<=20)
      {
    	  System.out.println(b);
    	  b=b+2;
      }
      
      // i want to print 5 table
      int c=5;
      while (c<=50)
      {
    	 System.out.println(c);
    	 c=c+5;
      }
    	// table f 3 in reverse 
     int d=30;
     while(d>=3)
     {
    	 System.out.println(d);
    	 d=d-3;
     }
     
     
     
	}
	
	
	

}
